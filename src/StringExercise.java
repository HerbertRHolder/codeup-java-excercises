import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String s1 = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(s1);
        String s2 = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(s2);
        String s3 = "In windows, the main drive is usually C:\\\n";
        System.out.println(s3);
        String s4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(s4);
    }
}
